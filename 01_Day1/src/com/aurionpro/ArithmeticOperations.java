package com.aurionpro;

public class ArithmeticOperations {

	public static void main(String[] args) {
		int number1 = 100;
		int number2 = 200;
		int sum = number1 + number2;
		int subtraction = number1 - number2;
		int product = number1 * number2;
		int divide = number1 % number2;

		System.out.println("sum of number1 and number2 is" + sum);
		System.out.println("subtraction of number1 and number2 is" + subtraction);
		System.out.println("product of number1 and number2 is" + product);
		System.out.println("divide of number1 and number2 is" + divide);

	}

}
