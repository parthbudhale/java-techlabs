package com.model;

public class Calculator {
	public double add(double num1,double num2)
	{
		return num1+num2;
	}
	
	public double muliply(double num1,double num2)
	{
		return num1*num2;
	}
	
	public double sub(double num1,double num2)
	{
		return num1-num2;
	}
	public double div(double num1,double num2)
	{
		return num1%num2;
	}



}
